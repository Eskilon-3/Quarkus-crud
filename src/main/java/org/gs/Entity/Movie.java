package org.gs.Entity;


import javax.persistence.*;

@Entity
@Table(name = "Movie")
public class Movie {

    @Id @GeneratedValue
    private Long id;

    @Column(length = 100)
    private String title;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    @Override
    public String toString() {
        return "Movie{" +
                "id=" + id +
                ", title='" + title + '\'' +
                '}';
    }
}
